drop table if exists news cascade;
drop table if exists rss_feed cascade;
drop table if exists object_xslt cascade;

CREATE TABLE news
(
    id_news     int8 generated by default as identity unique,
    tittle      varchar(70),
    link        varchar(70),
    description varchar(70),
    pubDate     varchar(70),
    primary key (id_news)
);

CREATE TABLE rss_feed
(
    id_rss           int8 generated by default as identity unique,
    current_position boolean,
    url              varchar(70),
    date_created     timestamp,
    date_updated     timestamp,
    primary key (id_rss)
);

create table object_xslt
(
    id_object_xslt int8 generated by default as identity unique,
    id_news        INTEGER REFERENCES news (id_news),
    object         varchar(600),
    primary key (id_object_xslt)
);

create table rss_news
(
    id_rss_news int8 generated by default as identity unique,
    id_news     INTEGER REFERENCES news (id_news),
    id_rss      INTEGER REFERENCES rss_feed (id_rss),
    primary key (id_rss_news)
);

